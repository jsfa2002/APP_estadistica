import streamlit as st
import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression, LogisticRegression
from sklearn.discriminant_analysis import LinearDiscriminantAnalysis, QuadraticDiscriminantAnalysis
from sklearn.metrics import confusion_matrix, accuracy_score, classification_report

# Configuraci贸n de la p谩gina
st.set_page_config(page_title="ReDim - An谩lisis de Regresi贸n", layout="wide")
st.title(' ReDim: An谩lisis de Regresi贸n y Predicci贸n')

# Sidebar para subir el archivo
st.sidebar.header("Opciones")
uploaded_file = st.sidebar.file_uploader("Sube tu archivo CSV", type=["csv"])

if uploaded_file is not None:
    df = pd.read_csv(uploaded_file)
    st.sidebar.success("Archivo cargado exitosamente!")

    st.subheader(" Vista previa de los datos")
    st.dataframe(df.head())

    numeric_cols = df.select_dtypes(include=['float64', 'int64']).columns
    cat_cols = df.select_dtypes(include=['object']).columns

    st.sidebar.subheader("Modelos de Regresi贸n y Predicci贸n")
    model_choice = st.sidebar.selectbox("Selecciona el modelo", ["Regresi贸n Lineal", "Regresi贸n Log铆stica", "LDA", "QDA"])

    target_var = st.sidebar.selectbox("Selecciona la variable dependiente (Y)", df.columns)
    predictor_vars = st.sidebar.multiselect("Selecciona las variables predictoras (X)", df.columns.drop(target_var))

    if st.sidebar.button("Ejecutar Modelo"):
        st.subheader(f" Modelo seleccionado: {model_choice}")

        X = df[predictor_vars]
        y = df[target_var]

        # Partici贸n del 70% entrenamiento y 30% prueba
        X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.3, random_state=42)

        if model_choice == "Regresi贸n Lineal":
            model = LinearRegression()
        elif model_choice == "Regresi贸n Log铆stica":
            model = LogisticRegression()
        elif model_choice == "LDA":
            model = LinearDiscriminantAnalysis()
        else:
            model = QuadraticDiscriminantAnalysis()

        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)

        st.write("### Resultados estimados")
        st.write(y_pred)

        if model_choice != "Regresi贸n Lineal":
            st.write("### Matriz de Confusi贸n")
            cm = confusion_matrix(y_test, y_pred)
            st.write(cm)

            st.write("### Reporte de Clasificaci贸n")
            st.write(classification_report(y_test, y_pred))

        st.success(f"{model_choice} ejecutado correctamente")

    # Comparaci贸n de modelos
    if st.sidebar.button("Comparar Modelos"):
        st.subheader(" Comparaci贸n de Modelos")
        models = {
            "Regresi贸n Log铆stica": LogisticRegression(),
            "LDA": LinearDiscriminantAnalysis(),
            "QDA": QuadraticDiscriminantAnalysis()
        }
        results = {}
        for name, mdl in models.items():
            mdl.fit(X_train, y_train)
            pred = mdl.predict(X_test)
            acc = accuracy_score(y_test, pred)
            results[name] = acc

        results_df = pd.DataFrame(results.items(), columns=["Modelo", "Exactitud"])
        st.write(results_df)

        best_model = results_df.loc[results_df['Exactitud'].idxmax()]
        st.success(f"El mejor modelo es {best_model['Modelo']} con una exactitud de {best_model['Exactitud']:.2f}")

